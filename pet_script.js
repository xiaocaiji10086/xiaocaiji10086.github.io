document.addEventListener('DOMContentLoaded', function() {
    const petInfo = document.getElementById('petInfo');
    const navLinks = document.querySelectorAll('nav a');
    const searchInput = document.getElementById('searchInput');
    const searchButton = document.getElementById('searchButton');

    function showPetInfo(petType) {
        const pets = petData[petType];
        let html = `
            <div class="category">
                <h2>${getPetTypeName(petType)}</h2>
                <div class="category-options">
                    ${pets.map(pet => `
                        <button class="category-option" data-pet-name="${pet.name}" data-pet-type="${petType}">
                            <span class="category-option-icon"><i class="fas ${pet.icon}"></i></span>
                            ${pet.name}
                        </button>
                    `).join('')}
                </div>
            </div>
        `;
        petInfo.innerHTML = html;
        addPetButtonListeners();
    }

    function addPetButtonListeners() {
        document.querySelectorAll('.category-option').forEach(button => {
            button.addEventListener('click', function() {
                const petName = this.getAttribute('data-pet-name');
                const petType = this.getAttribute('data-pet-type');
                showPetDetails(petType, petName);
            });
        });
    }

    function showPetDetails(petType, petName) {
        const petDetails = petDatabase[petType][petName];
        if (petDetails) {
            petInfo.innerHTML = `
                <div class="pet-details">
                    <h2>${petName}</h2>
                    <p>${petDetails}</p>
                    <button class="back-button">返回</button>
                </div>
            `;
            document.querySelector('.back-button').addEventListener('click', () => showPetInfo(petType));
        }
    }

    function getPetTypeName(petType) {
        const names = {
            dogs: '狗狗',
            cats: '猫咪',
            birds: '鸟类',
            rabbits: '兔子',
            fish: '鱼类'
        };
        return names[petType] || petType;
    }

    navLinks.forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            showPetInfo(this.getAttribute('data-pet'));
        });
    });

    function searchPets() {
        const searchTerm = searchInput.value.toLowerCase().trim();
        const results = Object.entries(petData).flatMap(([type, pets]) => 
            pets.filter(pet => pet.name.toLowerCase().includes(searchTerm))
                .map(pet => ({...pet, type}))
        );

        if (results.length > 0) {
            petInfo.innerHTML = `
                <div class="category">
                    <h2>搜索结果</h2>
                    <div class="category-options">
                        ${results.map(pet => `
                            <button class="category-option" data-pet-name="${pet.name}" data-pet-type="${pet.type}">
                                <span class="category-option-icon"><i class="fas ${pet.icon}"></i></span>
                                ${pet.name}
                            </button>
                        `).join('')}
                    </div>
                </div>
            `;
            addPetButtonListeners();
        } else {
            petInfo.innerHTML = '<p>没有找到匹配的宠物。</p>';
        }
    }

    searchButton.addEventListener('click', searchPets);
    searchInput.addEventListener('keypress', function(e) {
        if (e.key === 'Enter') {
            searchPets();
        }
    });

    showPetInfo('dogs');
});